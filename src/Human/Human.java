package Human;

public class Human {
	private int id;
	private String Name;
	private String LastName;
	
	public int getId() {
		return id;
	}
	public String getName() {
		return Name;
	}
	public String getLastName() {
		return LastName;
	}
	public Human(int id, String name, String lastName) {
		super();
		this.id = id;
		Name = name;
		LastName = lastName;
	}
	
	

}
